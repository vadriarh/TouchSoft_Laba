package app.main;

class Message{
    private String status;
    private String name;
    private String text;


    String getStatus() {
        return status;
    }

    void setStatus(String status) {
        this.status = status;
    }

    String getName() {
        return name;
    }

    void setName(String name) {
        this.name = name;
    }

    String getText() {
        return text;
    }

    void setText(String text) {
        this.text = text;
    }
}
